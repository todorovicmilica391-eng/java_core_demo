package com.itakademija.three;

public class CharacterDemo {
    static void main(String[] args) {
        char slovo = 's';
        System.out.println("Slovo = " + slovo);
        char slovo2 = 65; // A
        System.out.println("Slovo = " + slovo2);

        int number = slovo2;
        System.out.println("Number = " + number);
    }
}
